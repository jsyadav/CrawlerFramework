# http://wiki.python.org/moin/PythonDecoratorLibrary
def simple_decorator(decorator):
    """This decorator can be used to turn simple functions
    into well-behaved decorators, so long as the decorators
    are fairly simple. If a decorator expects a function and
    returns a function (no descriptors), and if it doesn't
    modify function attributes or docstring, then it is
    eligible to use this. Simply apply @simple_decorator to
    your decorator and it will automatically preserve the
    docstring and function attributes of functions to which
    it is applied."""
    def new_decorator(f):
        g = decorator(f)
        g.__name__ = f.__name__
        g.__doc__ = f.__doc__
        g.__dict__.update(f.__dict__)
        return g
    # Now a few lines needed to make simple_decorator itself
    # be a well-behaved decorator.
    new_decorator.__name__ = decorator.__name__
    new_decorator.__doc__ = decorator.__doc__
    new_decorator.__dict__.update(decorator.__dict__)
    return new_decorator

# Decorate functions with args
# http://aspn.activestate.com/ASPN/Cookbook/Python/Recipe/465427
decorator_with_args = lambda decorator: lambda *args, **kwargs: lambda func: decorator(func, *args, **kwargs)

@decorator_with_args
def logit(fn, log, fname):
    '''Log a function on entry and exit.

    log - log object obtained from python logger module
    fname - a string denoting the function name which is called
    '''
    def _decorate(*args, **kwargs):
        #print 'in decorator'
        log.info("Entry into " + fname)
        ret = fn(*args, **kwargs)
        log.info("Exit of " + fname)
        return ret
    return _decorate
